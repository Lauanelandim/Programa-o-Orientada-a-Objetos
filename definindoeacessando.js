/**
 * Propriedades e métodos podem ser definidos diretamente no objeto
usando pares chave: valor. Elas podem ser acessadas usando
objeto.propriedade ou objeto['propriedade'].
 */


// Acessando Propriedades:
console.log(car.model); //Saída: Corolla
console.log(car['year']); //Saída: 2020

//Chamando métodos:
car.displayInfo(); //Executa o método displayInfo (mostrar configuração)
